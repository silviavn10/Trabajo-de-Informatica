void Muñeco::TeclaEspecial(unsigned char key)
{
	switch (key)
	{
	case GLUT_KEY_LEFT:
		SetVel(-5.0f, 0.0f);
		break;
	case GLUT_KEY_RIGHT:
		SetVel(5.0f, 0.0f);
		break;
	case GLUT_KEY_UP:
		for (int i = 0; i < MAX_PLATAFORMAS; i++) {
			if (-0.1 < (getPosY() - (plataformas.GetLimiteY2())) < 0.1) setVelY(15.0f);
		}
		if (-0.1 < (getPosY() - (plataformas.GetLimiteY2())) < 0.1) setVelY(15.0f);
		if ((getPosY() - suelo.GetSueloLimiteY2()) < 0.05) {
			setVelY(15.0f);
		}
		break;
	}
}
