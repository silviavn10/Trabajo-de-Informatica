void Muñeco::Mueve(float t) //Funcion para que el muñeco tenga movimiento
{
	activo->loop(); //Funcion interna de los sprites
	posicion = posicion + velocidad * t + aceleracion*(0.5f * t * t);
	velocidad = velocidad + aceleracion * t;
}

